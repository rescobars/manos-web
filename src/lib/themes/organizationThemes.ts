// Función helper para obtener tema por UUID de organización
export function getThemeByOrganizationUuid(uuid: string): OrganizationThemeConfig | null {
  return organizationThemes[uuid] || null;
}

// Función helper para obtener colores por UUID de organización
export function getThemeColorsByOrganizationUuid(uuid: string): DynamicThemeColors | null {
  const theme = getThemeByOrganizationUuid(uuid);
  return theme ? theme.colors : null;
}
